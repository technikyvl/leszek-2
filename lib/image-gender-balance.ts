// Helper function to get balanced images for a section
export function getBalancedImages(count: number, section: 'hero' | 'services' | 'gallery' = 'gallery'): string[] {
  let source: string[];
  switch (section) {
    case 'hero':
      source = heroPortraits;
      break;
    case 'services':
      source = servicePortraits;
      break;
    default:
      source = galleryPortraits;
  }
  
  // Shuffle and return requested count
  const shuffled = [...source].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

